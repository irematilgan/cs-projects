CODESG  SEGMENT PARA 'CODE'
        ORG 100H
        ASSUME CS:CODESG, SS:CODESG, DS:CODESG, ES:CODESG
ANA:
	JMP BASLA
DIZI	DW -100, 100, 10, 511, 0
ELEMAN	DW 5
YER	DW 0
BASLA   PROC NEAR
	
	MOV CX, ELEMAN
	DEC CX
	XOR SI, SI
	XOR DX, DX
TEKRAR:	MOV AX, DIZI[SI]
	SUB AX, DIZI[SI+2]
	JNS DEVAM
	NEG AX
DEVAM:	CMP AX, DX
	JBE L1
	MOV DX, AX
	MOV YER, SI
L1:	ADD SI, 2
	LOOP TEKRAR		

        RET
BASLA   ENDP


CODESG  ENDS
        END ANA

PUBLIC GCD_PROC 
EXTRN DENEME:WORD 

DATASG  SEGMENT PARA PUBLIC 'DATA'
DENEME2  DW -1
DATASG ENDS

CODESG  SEGMENT PARA PUBLIC 'CODE'
        ASSUME CS:CODESG, DS:DATASG

GCD_PROC PROC
        CMP AX, DX
        JA DEVAM
        XCHG AX,DX
DEVAM:  CMP DX, 0
        JA RECUR
        JMP EXIT
RECUR:  MOV BX, DX
        XOR DX, DX
        DIV BX
        MOV AX, BX
        CALL GCD_PROC  
EXIT:   RET
GCD_PROC ENDP

CODESG  ENDS
        END
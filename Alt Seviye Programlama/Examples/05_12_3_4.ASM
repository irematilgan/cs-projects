        TITLE DIZI_MAX_F


PUBLIC DIZI_MAX_F  
EXTRN DIZI:WORD, ELEMAN:WORD

CODESG  SEGMENT PARA PUBLIC 'CODE'
        ASSUME CS:CODESG  

DIZI_MAX_F PROC NEAR
	PUSH CX
	PUSH SI
        LEA SI, DIZI
        MOV CX, ELEMAN
        MOV AX, [SI]
        DEC CX
TEKRAR: ADD SI,2
        CMP AX, [SI]
        JA L1
        MOV AX, [SI]
L1:     LOOP TEKRAR
	POP SI
	POP CX
        RET
DIZI_MAX_F ENDP

CODESG    ENDS
        END
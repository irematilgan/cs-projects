CODESG SEGMENT PARA 'CD'
	ASSUME CS: CODESG, DS: DATASG, SS: STKSG
	SON PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX

		MOV AX, DATASG
		MOV DS, AX
	
		MOV CX, ELEMAN
		XOR SI, SI
	L1:	
		MOV BX, GIRIS[SI]
		
		SHL BX, 1; x2 	
		INC CIKIS[BX]	

		ADD SI, 2
		
		LOOP L1
		RETF
	SON ENDP
CODESG ENDS
	END SON
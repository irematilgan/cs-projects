STKSG SEGMENT PARA STACK 'STK'
	DW 20 DUP (?)
STKSG ENDS

DATASG SEGMENT PARA 'DAT'
	DIZI DB -5, 3, 2, 10, -1, -10
	ELEMAN DW 6
	SONUC DB -128
DATASG ENDS

CODESG SEGMENT PARA 'CD'
	ASSUME CS: CODESG, DS: DATASG, SS: STKSG
	SON PROC FAR
		PUSH DS
		XOR AX, AX
		PUSH AX

		MOV AX, DATASG
		MOV DS, AX
		MOV CX, ELEMAN
		LEA SI, DIZI
		L1:
		MOV AL, [SI]
		CMP SONUC, AL
		JGE NEXT
		MOV SONUC, AL
		NEXT:
		INC SI
		
		LOOP L1

		RETF
	SON ENDP
CODESG ENDS
	END SON
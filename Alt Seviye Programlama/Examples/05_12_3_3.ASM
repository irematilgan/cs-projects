        TITLE DIZI_ORT_F


PUBLIC DIZI_ORT_F
EXTRN DIZI:WORD, ELEMAN:WORD


CODESG  SEGMENT PARA PUBLIC 'CODE'
        ASSUME CS:CODESG  

DIZI_ORT_F PROC NEAR

	PUSH CX
	PUSH BX
	PUSH SI
	PUSH DX
        LEA SI, DIZI
        MOV CX, ELEMAN
        MOV BX, CX
        XOR AX, AX
        XOR DX, DX
TEKRAR: ADD AX, [SI]
        ADC DX, 0
        ADD SI,2
L1:     LOOP TEKRAR
        DIV BX
	POP DX
	POP SI
	POP BX
	POP CX
        RET
DIZI_ORT_F ENDP

CODESG    ENDS
        END
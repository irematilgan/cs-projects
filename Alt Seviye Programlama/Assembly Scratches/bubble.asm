
STEK SEGMENT PARA STACK 'STK'
	DW 30 DUP(?)
STEK ENDS

DATS SEGMENT PARA 'DTS'
	dizi DB 4,7,5,8,3 DUP(6)
DATS ENDS

CSG SEGMENT PARA 'CODE'
		ASSUME 'SS':STEK,'DS':DATS,'CS':CSG
BASLA PROC FAR
	MOV CX,04H
	MOV SI,0H
	L1:	
		PUSH CX
		MOV DI,SI
	L2:	
		INC DI
		MOV AX,dizi[SI]
		CMP AX,dizi[DI]
		JNGE NEXT
		XCHG AX,dizi[DI]
		MOV dizi[SI],AX
	NEXT:	
		LOOP L2
		POP CX
		INC SI
		LOOP L1	
		
		RETF
BASLA ENDP
CSG ENDS
	END BASLA